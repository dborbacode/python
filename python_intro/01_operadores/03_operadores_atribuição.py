# Operadores de atribuição são usados para atribuir valores a variáveis. O operador de atribuição mais comum é o sinal de igual (=), 
# mas existem outros operadores de atribuição compostos que combinam uma operação com a atribuição.

saldo = 500
saldo += 100  # Equivalente a saldo = saldo + 100
print(saldo)  # 600


saldo = saldo // 2
print(saldo)  # 300