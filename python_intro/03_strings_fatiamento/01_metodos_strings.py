curso = "HellO woRld   "

print(curso.lower())  # Deixa todas as letras minúsculas
print(curso.upper())  # Deixa todas as letras maiúsculas            
print(curso.title())  # Deixa a primeira letra de cada palavra maiúscula
print(curso.capitalize())  # Deixa a primeira letra da string maiúscula e o restante minúsculo
print(curso.strip())  # Remove os espaços em branco no início e no final da string
print(curso.lstrip())  # Remove os espaços em branco no início da string                
print(curso.rstrip())  # Remove os espaços em branco no final da string
print(curso.center(10))  # Centraliza a string em um campo de largura 10
print(".".join(curso))  # Junta os caracteres da string com um ponto entre eles
print(curso.split())  # Divide a string em uma lista de palavras