"""
Funções são blocos de código reutilizáveis que realizam uma tarefa específica. 
Elas permitem organizar o código, evitar repetição e melhorar a legibilidade. 
Em Python, as funções são definidas usando a palavra-chave `def`, 
seguida pelo nome da função e parênteses que podem conter parâmetros.
"""

def saudacao(nome):
    """Função que recebe um nome e retorna uma saudação personalizada."""
    return f"Olá, {nome}! Bem-vindo(a) ao mundo da programação!"  

print(saudacao("Alice")) # Chama a função com o argumento "Alice"


